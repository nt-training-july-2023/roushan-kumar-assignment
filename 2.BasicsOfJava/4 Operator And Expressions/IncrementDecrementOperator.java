public class IncrementDecrementOperator {

    public static void main(String[] args) {
        int num1=6;

        System.out.println("value of num1              : "+ num1);
        System.out.println("Printing  num1++           : "+ num1++);
        System.out.println("after  num++ value of num1 : "+ num1);
        System.out.println("Printing  --num1           : "+ --num1);
        System.out.println();

        int num2=8;
        num2++;
        System.out.println("value of num2              : "+ num2);
        System.out.println();

        int num3=8;
        num3--;
        System.out.println("value of num3              : "+ num3);
        System.out.println();

        int num4=6;
        System.out.println("value of num4              : "+ num4);
        System.out.println("Printing  num4--           : "+ num4--);
        System.out.println("after  num-- value of num4 : "+ num4);
        System.out.println("Printing  --num4           : "+ --num4);
        System.out.println();

        

    }
    
}
